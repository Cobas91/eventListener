package eventlistener.security.service;

import eventlistener.security.repo.AppUserRepo;
import org.springframework.security.core.userdetails.User;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.stereotype.Service;


@Service
public class AppUserDetailService implements UserDetailsService {
    private final AppUserRepo appUserRepo;

    public AppUserDetailService(AppUserRepo appUserRepo) {
        this.appUserRepo = appUserRepo;
    }

    @Override
    public UserDetails loadUserByUsername(String username) throws UsernameNotFoundException {
        return appUserRepo.findByUsername(username)
                .map(appUser -> User
                .withUsername(username)
                .password(appUser.getPassword())
                .authorities("user")
                .build())
                .orElseThrow(()-> new UsernameNotFoundException("Username does not exist: "+username));
    }
}
